#!/usr/bin/env node
import { readFile, readdir, mkdir, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join } from 'node:path';
import ts from 'typescript';

const root = new URL('..', import.meta.url).pathname;
const packagesDir = join(root, 'packages');
const apiDir = join(root, 'etc', 'api');
const update = process.argv.includes('--write');
const entries = [];

for (const directory of (await readdir(packagesDir)).sort()) {
  const manifestPath = join(packagesDir, directory, 'package.json');
  if (!existsSync(manifestPath)) continue;
  const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));
  if (manifest.private) continue;
  for (const [subpath, target] of Object.entries(manifest.exports ?? {})) {
    const types = typeof target === 'object' && target ? target.types : undefined;
    if (typeof types !== 'string') throw new Error(`${manifest.name} ${subpath} has no types export`);
    const file = join(packagesDir, directory, types.replace(/^\.\//, ''));
    if (!existsSync(file)) throw new Error(`Missing declarations for ${manifest.name}${subpath === '.' ? '' : subpath.slice(1)}. Run pnpm build first.`);
    entries.push({ packageName: manifest.name, directory, subpath, file });
  }
}

const program = ts.createProgram(entries.map((entry) => entry.file), {
  target: ts.ScriptTarget.ES2022,
  module: ts.ModuleKind.NodeNext,
  moduleResolution: ts.ModuleResolutionKind.NodeNext,
  skipLibCheck: true,
});
const checker = program.getTypeChecker();
const inventory = [];
const reports = new Map();

for (const entry of entries) {
  const source = program.getSourceFile(entry.file);
  const moduleSymbol = source && checker.getSymbolAtLocation(source);
  if (!source || !moduleSymbol) throw new Error(`Cannot inspect ${entry.file}`);
  const report = [];
  for (const exported of checker.getExportsOfModule(moduleSymbol).sort((a, b) => a.name.localeCompare(b.name))) {
    const symbol = exported.flags & ts.SymbolFlags.Alias ? checker.getAliasedSymbol(exported) : exported;
    const declaration = symbol.valueDeclaration ?? symbol.declarations?.[0];
    const kind = declaration ? ts.SyntaxKind[declaration.kind] : 'Unknown';
    const signature = renderSymbol(checker, symbol, declaration);
    const classification = classify(entry, exported.name);
    const item = {
      package: entry.packageName,
      entrypoint: entry.subpath,
      name: exported.name,
      kind,
      classification,
      signature,
    };
    inventory.push(item);
    report.push(item);
  }
  reports.set(reportName(entry), renderReport(entry, report));
}

inventory.sort((a, b) => `${a.package}:${a.entrypoint}:${a.name}`.localeCompare(`${b.package}:${b.entrypoint}:${b.name}`));
const outputs = new Map([
  [join(apiDir, 'public-api-inventory.json'), `${JSON.stringify({ schemaVersion: 1, exports: inventory }, null, 2)}\n`],
  ...[...reports].map(([name, content]) => [join(apiDir, name), content]),
]);

if (update) {
  await mkdir(apiDir, { recursive: true });
  for (const [path, content] of outputs) await writeFile(path, content, 'utf8');
  console.log(`Wrote ${reports.size} API reports and ${inventory.length} classified exports.`);
} else {
  for (const [path, expected] of outputs) {
    if (!existsSync(path) || await readFile(path, 'utf8') !== expected) {
      throw new Error(`Public API report is stale: ${path}. Run pnpm api:extract.`);
    }
  }
  console.log(`Verified ${reports.size} deterministic API reports and ${inventory.length} classified exports.`);
}

function renderSymbol(checker, symbol, declaration) {
  try {
    if (symbol.flags & (ts.SymbolFlags.Interface | ts.SymbolFlags.TypeAlias | ts.SymbolFlags.TypeParameter)) {
      return checker.typeToString(checker.getDeclaredTypeOfSymbol(symbol), declaration, ts.TypeFormatFlags.NoTruncation);
    }
    return checker.typeToString(checker.getTypeOfSymbolAtLocation(symbol, declaration), declaration, ts.TypeFormatFlags.NoTruncation);
  } catch {
    return symbol.name;
  }
}

function classify(entry, name) {
  if (entry.subpath === './experimental') return 'experimental';
  if (entry.subpath === './diagnostics') return 'advanced-diagnostics';
  if (entry.subpath === './testing' || entry.packageName.endsWith('-testing')) return 'testing-only';
  if (/sanitize|diagnostic/i.test(name)) return 'advanced-diagnostics';
  return 'stable-for-alpha';
}

function reportName(entry) {
  const suffix = entry.subpath === '.' ? '' : `-${entry.subpath.slice(2).replaceAll('/', '-')}`;
  return `${entry.directory}${suffix}.api.md`;
}

function renderReport(entry, exports) {
  const title = `${entry.packageName}${entry.subpath === '.' ? '' : entry.subpath.slice(1)}`;
  const lines = [
    `# ${title} public API`,
    '',
    '> Generated by `pnpm api:extract`. Review and commit intentional changes.',
    '',
  ];
  for (const item of exports) {
    lines.push(`- **${item.name}** — ${item.classification} — \`${item.kind}\``, `  - \`${item.signature.replaceAll('`', '\\`')}\``);
  }
  return `${lines.join('\n')}\n`;
}
